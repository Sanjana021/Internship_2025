#Create a program that takes two numbers and an operator (+, -, *, /, //, %, **) as input and performs the corresponding operation.
num1=10
num2=20
operator="%"
if operator=="+":
    print(num1+num2)
if operator=="-":
    print(num1-num2)
if operator=="*":
    print(num1*num2)
if operator=="/":
    print(num1/num2)
if operator=="%":
    print(num1%num2)




